    @Test
    public void testEqualsByteArrays() {
        byte[] a = {1, 2, 3};
        byte[] b = {1, 2, 3};
        byte[] c = {1, 2, 4};
        Assert.assertTrue(Bytes.equals(a, 0, a.length, b, 0, b.length));
        Assert.assertFalse(Bytes.equals(a, 0, a.length, c, 0, c.length));
    }

